# Pilas


pila = [1,2,3]

# Agregar elementos al final
pila.append(4)
pila.append(5)
print(pila)

# Sacar elementos por el final
n = pila.pop()
print(f"Sacando el elemento {n}")

print(pila)


# Colas


cola =  ["Maria","Marcos","Alfonso","Lionel"]

# Agregamos elementos al final de la cola
cola.append("Fausto")
cola.append("Flor")
print(cola)

# Sacando elementos por el principio de la cola
n = cola.pop(0)
print(f"Estan atendiendo a {n}")
print(cola)
